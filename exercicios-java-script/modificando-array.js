console.log(frutas.length); //quantidade de valores dentro da array
console.log(frutas.push("pera")); // atribuindo ao final da array num novo valor
console.log(frutas); //mostrando na tela todos elementos da array
console.log(frutas.slice(1, 3)); //fatiando o valor do indice 1 e o valor do indice 3
console.log(frutas.push('coco', 'morango', 'açai', 'mamao'));
